package com.ae.proyecto;

public class Solucion {
    private int chapa_22;
    private int chapa_24;
    private int chapa_26;

    public Solucion(int chapa22, int chapa24, int chapa26) {
        this.chapa_22 = chapa22;
        this.chapa_24 = chapa24;
        this.chapa_26 = chapa26;
    }

    public int getChapa22() { return this.chapa_22; }
    public int getChapa24() { return this.chapa_24; }
    public int getChapa26() { return this.chapa_26; }
}
